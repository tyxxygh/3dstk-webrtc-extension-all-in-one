#include <config.h>

#include <stddef.h>
#include <stdio.h>
#include <stdlib.h>
#include <string.h>

#ifdef HAVE_LOCALE_H
#include <locale.h>
#endif

#include <idn/res.h>

int
main(int ac, char *av[]) {
	idn_result_t r = idn_success;
	idn_resconf_t ctx = NULL;
	idn_action_t actions = 0;
	const char *conffile = NULL;
	const char *localcheckfile = NULL;
	const char *auxencoding = NULL;
	const char *from;
	char to[1024];
	int i;

#ifdef HAVE_SETLOCALE
	setlocale(LC_ALL, "");
#endif

	for (i = 1; i < ac && *av[i] == '-'; i++) {
		if (strcmp(av[i], "--") == 0) {
			i++;
			break;
		}
		if (strcmp(av[i], "-decode-regist") == 0)
			actions |= IDN_DECODE_REGIST;
		else if (strcmp(av[i], "-decode-lookup") == 0)
			actions |= IDN_DECODE_LOOKUP;
		else if (strcmp(av[i], "-unicodeconv") == 0)
			actions |= IDN_UNICODECONV;
		else if (strcmp(av[i], "-map") == 0)
			actions |= IDN_MAP;
		else if (strcmp(av[i], "-asclower") == 0)
			actions |= IDN_ASCLOWER;
		else if (strcmp(av[i], "-idnconv") == 0)
			actions |= IDN_IDNCONV;
		else if (strcmp(av[i], "-prohcheck") == 0)
			actions |= IDN_PROHCHECK;
		else if (strcmp(av[i], "-unascheck") == 0)
			actions |= IDN_UNASCHECK;
		else if (strcmp(av[i], "-nfccheck") == 0)
			actions |= IDN_NFCCHECK;
		else if (strcmp(av[i], "-prefcheck") == 0)
			actions |= IDN_PREFCHECK;
		else if (strcmp(av[i], "-hyphcheck") == 0)
			actions |= IDN_HYPHCHECK;
		else if (strcmp(av[i], "-combcheck") == 0)
			actions |= IDN_COMBCHECK;
		else if (strcmp(av[i], "-ctxjcheck") == 0)
			actions |= IDN_CTXJCHECK;
		else if (strcmp(av[i], "-ctxocheck") == 0)
			actions |= IDN_CTXOCHECK;
		else if (strcmp(av[i], "-ctxolitecheck") == 0)
			actions |= IDN_CTXOLITECHECK;
		else if (strcmp(av[i], "-bidicheck") == 0)
			actions |= IDN_BIDICHECK;
		else if (strcmp(av[i], "-localcheck") == 0)
			actions |= IDN_LOCALCHECK;
		else if (strcmp(av[i], "-rtcheck") == 0)
			actions |= IDN_RTCHECK;
		else if (strcmp(av[i], "-localconv") == 0)
			actions |= IDN_LOCALCONV;
		else if (strcmp(av[i], "-undoiferr") == 0)
			actions |= IDN_UNDOIFERR;
		else if (strncmp(av[i], "-conffile=", 10) == 0)
			conffile = av[i] + 10;
		else if (strncmp(av[i], "-localcheckfile=", 16) == 0)
			localcheckfile = av[i] + 16;
		else if (strncmp(av[i], "-auxencoding=", 13) == 0)
			auxencoding = av[i] + 13;
		else {
			printf("FATAL: bad option '%s'\n", av[i]);
			return (1);
		} 
	}
	if (av[i] == NULL) {
		printf("FATAL: too few argument\n");
		return (1);
	}

	from = av[i];
	to[0] = '\0';
	if (conffile != NULL && *conffile == '\0')
		conffile = NULL;
	if (localcheckfile != NULL && *localcheckfile == '\0')
		localcheckfile = NULL;
	if (auxencoding != NULL && *auxencoding == '\0')
		auxencoding = NULL;

	printf("auxencoding: %s\n", (auxencoding != NULL) ? auxencoding : "");
	printf("from: %s\n", from);

	r = idn_resconf_initialize();
	if (r != idn_success) {
		printf("result: idn_resconf_initialize() ERROR: %s\n",
		       idn_result_tostring(r));
		goto ret;
	}
	r = idn_resconf_create(&ctx);
	if (r != idn_success) {
		printf("result: idn_resconf_create() ERROR: %s\n",
		       idn_result_tostring(r));
		goto ret;
	}
	r = idn_resconf_loadfile(ctx, conffile);
	if (r != idn_success) {
		printf("result: idn_resconf_loadfile() ERROR: %s\n",
		       idn_result_tostring(r));
		goto ret;
	}
	if (localcheckfile != NULL && (actions & IDN_LOCALCHECK)) {
		r = idn_resconf_setlocalcheckfile(ctx, localcheckfile);
		if (r != idn_success) {
			printf("result: idn_setlocalcheckfile() ERROR: %s\n",
				idn_result_tostring(r));
			goto ret;
		}
	}
	r = idn_res_decodename2(ctx, actions, from, to, sizeof(to),
				auxencoding);
	if (r != idn_success)
		to[0] = '\0';
	printf("result: %s\n", idn_result_tostring(r));

ret:
	printf("to: %s\n", to);

	if (ctx != NULL)
		idn_resconf_destroy(ctx);
	return (0);
}
