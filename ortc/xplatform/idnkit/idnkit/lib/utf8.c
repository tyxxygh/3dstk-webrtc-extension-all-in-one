#include <config.h>

#include <stddef.h>
#include <stdlib.h>
#include <string.h>

#include <idn/assert.h>
#include <idn/debug.h>
#include <idn/logmacro.h>
#include <idn/result.h>
#include <idn/utf8.h>

#define UTF8_WIDTH(c) \
	(((c) < 0x80) ? 1 : \
	 ((c) < 0xc0) ? 0 : \
	 ((c) < 0xe0) ? 2 : \
	 ((c) < 0xf0) ? 3 : \
	 ((c) < 0xf8) ? 4 : \
	 ((c) < 0xfc) ? 5 : \
	 ((c) < 0xfe) ? 6 : 0)

#define VALID_CONT_BYTE(c)	(0x80 <= (c) && (c) < 0xc0)

/*
 * Determine number of bytes in next multibyte UTF-8 character.
 */
int
idn__utf8_mblen(const char *s) {
	unsigned long dummy;

	assert(s != NULL);
	TRACE(("idn__utf8_mblen(s=<%s>)\n", idn__debug_hexstring(s)));

	return (idn__utf8_mbtowc(s, &dummy));
}

/*
 * Convert a multibyte UTF-8 sequence to a wide Unicode character.
 */
int
idn__utf8_mbtowc(const char *s, unsigned long *vp) {
	unsigned long v;
	unsigned long min;
	const unsigned char *p = (const unsigned char *)s;
	int c;
	int width;
	int rest;

	assert(s != NULL);

	c = *p++;
	width = UTF8_WIDTH(c);

	switch (width) {
	case 0:
		return (0);
	case 1:
		v = c;
		min = 0;
		break;
	case 2:
		v = c & 0x1f;
		min = 0x80;
		break;
	case 3:
		v = c & 0xf;
		min = 0x800;
		break;
	case 4:
		v = c & 0x7;
		min = 0x10000;
		break;
	case 5:
		v = c & 3;
		min = 0x200000;
		break;
	case 6:
		v = c & 1;
		min = 0x4000000;
		break;
	default:
		FATAL(("idn__utf8_mbtowc: internal error\n"));
		return (0);
	}

	rest = width - 1;
	while (rest-- > 0) {
		if (!VALID_CONT_BYTE(*p))
			return (0);
		v = (v << 6) | (*p & 0x3f);
		p++;
	}

	if (v < min)
		return (0);

	*vp = v;
	return (width);
}

/*
 * Convert a wide Unicode character to a multibyte UTF-8 sequence.
 */
int
idn__utf8_wctomb(char *s, size_t len, unsigned long v) {
	unsigned char *p = (unsigned char *)s;
	int mask;
	int off;
	int l;

	assert(s != NULL);

	if (v < 0x80) {
		mask = 0;
		l = 1;
	} else if (v < 0x800) {
		mask = 0xc0;
		l = 2;
	} else if (v < 0x10000) {
		mask = 0xe0;
		l = 3;
	} else if (v < 0x200000) {
		mask = 0xf0;
		l = 4;
	} else if (v < 0x4000000) {
		mask = 0xf8;
		l = 5;
	} else if (v < 0x80000000) {
		mask = 0xfc;
		l = 6;
	} else {
		return (0);
	}

	if (len < (size_t)l)
		return (0);

	off = 6 * (l - 1);
	*p++ = (v >> off) | mask;
	mask = 0x80;
	while (off > 0) {
		off -= 6;
		*p++ = ((v >> off) & 0x3f) | mask;
	}
	return (l);
}

/*
 * Check if 's' begins with a valid multibyte UTF-8 sequences.
 */
int
idn__utf8_isvalidchar(const char *s) {
	unsigned long dummy;

	TRACE(("idn__utf8_isvalidchar(s=<%s>)\n",
	      idn__debug_hexstring(s)));

	return (idn__utf8_mbtowc(s, &dummy) > 0);
}

/*
 * Check if 's' consist of valid multibyte UTF-8 sequences.
 */
int
idn__utf8_isvalidstring(const char *s) {
	unsigned long dummy;
	int width;

	assert(s != NULL);

	TRACE(("idn__utf8_isvalidstring(s=<%s>)\n",
	      idn__debug_hexstring(s)));

	while (*s != '\0') {
		width = idn__utf8_mbtowc(s, &dummy);
		if (width == 0)
			return (0);
		s += width;
	}
	return (1);
}

/*
 * Find a first byte of a multibyte UTF-8 sequence.
 */
char *
idn__utf8_findfirstbyte(const char *s, const char *known_top) {
	const unsigned char *p = (const unsigned char *)s;
	const unsigned char *t = (const unsigned char *)known_top;

	assert(s != NULL && known_top != NULL && known_top <= s);

	TRACE(("idn__utf8_findfirstbyte(s=<%s>)\n",
	      idn__debug_hexstring(s)));

	while (p >= t) {
		if (!VALID_CONT_BYTE(*p))
		    break;
		p--;
	}
	if (p < t || UTF8_WIDTH(*p) == 0)
		return (NULL);

	return ((char *)p);
}
