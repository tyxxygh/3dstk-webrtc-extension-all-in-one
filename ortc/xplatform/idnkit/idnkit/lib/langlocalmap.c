/*
 * Language localmap entry.
 */
typedef struct langlocalmap_entry *idn__langlocalmap_entry_t;

struct langlocalmap_entry {
	idn__foreignmap_t map;  		/* map object */
	idn__langlocalmap_entry_t next;		/* pointer to next entry */
};

/*
 * Language localmap object.
 */
struct idn__langlocalmap {
	idn__lang_t lang;			/* current language */
	idn__strhash8_t hash;                   /* hash table for map
						 * entries */
	idn__langlocalmap_entry_t entries_head; /* map entries */
	idn__langlocalmap_entry_t entries_tail;
	idn__langlocalmap_entry_t default_map;  /* default map */
};

/*
 * Create a localmap object.
 */
idn_result_t
idn__langlocalmap_create(idn__langlocalmap_t *ctxp) {
	idn_result_t r = idn_success;
	idn__langlocalmap_t ctx = NULL;
	idn__lang_t new_lang = NULL;
	idn__strhash8_t new_hash = NULL;

	assert(ctxp != NULL);
	TRACE(("idn__langlocalmap_create()\n"));

	ctx = (idn__langlocalmap_t)malloc(sizeof(struct idn__langlocalmap));
	if (ctx == NULL) {
		r = idn_nomemory;
		goto ret;
	}

	ctx->lang = NULL;
	ctx->hash = NULL;
	ctx->entries_head = NULL;
	ctx->entries_tail = NULL;
	ctx->default_map  = NULL;

	r = idn__lang_create(&new_lang);
	if (r != idn_success)
		goto ret;
	ctx->lang = new_lang;
	
	r = idn__strhash8_create(&new_hash);
	if (r != idn_success)
		goto ret;
	ctx->hash = new_hash;

	*ctxp = ctx;

ret:
	if (r != idn_success) {
		if (new_lang != NULL)
			idn__lang_destroy(new_lang);
		if (new_hash != NULL)
			idn__strhash8_destroy(new_hash, NULL);
		free(ctx);
	}
	TRACE(("idn__langlocalmap_create(): %s\n", idn_result_tostring(r)));
	return (r);
}

/*
 * Release all the memory allocated for 'ctx'.
 */
void
idn__langlocalmap_destroy(idn__langlocalmap_t ctx) {
	idn__langlocalmap_entry_t entry;
	idn__langlocalmap_entry_t next;

	assert(ctx != NULL);

	TRACE(("idn__langlocalmap_destroy()\n"));

	if (ctx->entries_head != NULL) {
		entry = ctx->entries_head;
		while (entry != NULL) {
			next = entry->next;
			idn__foreignmap_destroy(entry->map);
			free(entry);
			entry = next;
		}
	}

	idn__strhash8_destroy(ctx->hash, NULL);
	idn__lang_destroy(ctx->lang);
	free(ctx);

	TRACE(("idn__langlocalmap_destroy(): the object is destroyed\n"));
}

const char *
idn__langlocalmap_getlang(idn__langlocalmap_t ctx) {
	return (idn__lang_getname(ctx->lang));
}

void
idn__langlocalmap_setlang(idn__langlocalmap_t ctx, const char *lang) {
	idn_result_t r = idn_success;

	assert(ctx != NULL);

	TRACE(("idn__langlocalmap_setlang(lang=\"%s\")\n", 
	       idn__debug_xstring(lang)));

	idn__lang_setname(ctx->lang, lang);

	TRACE(("idn__langlocalmap_setlang(): %s\n", idn_result_tostring(r)));
}

/*
 * Read a mapping definition file and add mappings in it to 'ctx'.
 */
idn_result_t
idn__langlocalmap_addfromfile(idn__langlocalmap_t ctx, const char *lang,
			  const char *file) {
	idn_result_t r = idn_success;
	idn__langlocalmap_entry_t new_entry = NULL;
	idn__foreignmap_t new_mapctx = NULL;
	idn__lang_t langctx = NULL;

	assert(ctx != NULL && file != NULL);

	TRACE(("idn__langlocalmap_addfromfile(lang=\"%s\", file=\"%s\")\n",
	       idn__debug_xstring(lang), idn__debug_xstring(file)));

	if (lang != NULL && strcmp(lang, "*") == 0)
		lang = NULL;
	if (strncmp(file, "filemap:", 8) == 0)
		file += 8;

	new_entry = (idn__langlocalmap_entry_t)malloc(sizeof(*new_entry));
	if (new_entry == NULL) {
		r = idn_nomemory;
		goto ret;
	}
	new_entry->map      = NULL;
	new_entry->next     = NULL;

	r = idn__foreignmap_create(&new_mapctx);
	if (r != idn_success)
		goto ret;
	r = idn__foreignmap_addfromfile(new_mapctx, file);
	if (r != idn_success)
		goto ret;
	new_entry->map = new_mapctx;

	if (lang != NULL) {
		r = idn__lang_create(&langctx);
		if (r != idn_success)
			goto ret;
		idn__lang_setname(langctx, lang);
		r = idn__strhash8_put(ctx->hash, idn__lang_getname(langctx),
				      new_entry);
		if (r != idn_success)
			goto ret;
	} else {
		ctx->default_map = new_entry;
	}

	if (ctx->entries_head == NULL) {
		ctx->entries_head = new_entry;
		ctx->entries_tail = new_entry;
	} else {
		ctx->entries_tail->next = new_entry;
		ctx->entries_tail = new_entry;
	}
	
ret:
	TRACE(("idn__langlocalmap_addfromfile(): %s\n",
	       idn_result_tostring(r)));
	if (r != idn_success) {
		if (new_mapctx != NULL)
			idn__foreignmap_destroy(new_mapctx);
		if (new_entry != NULL)
			free(new_entry);
	}
	if (langctx != NULL)
		idn__lang_destroy(langctx);
	return (r);
}

/*
 * Perform mapping.
 */
idn_result_t
idn__langlocalmap_map(idn__langlocalmap_t ctx, const unsigned long *from,
		      unsigned long *to, size_t tolen) {
	idn_result_t r = idn_success;
	idn__langlocalmap_entry_t entry = NULL;

	assert(ctx != NULL && from != NULL && to != NULL);

	TRACE(("idn__langlocalmap_map(from=\"%s\", tolen=%d)\n",
	       idn__debug_utf32xstring(from), (int)tolen));

	entry = (idn__langlocalmap_entry_t)
		idn__strhash8_get(ctx->hash, idn__lang_getname(ctx->lang));
	if (entry == NULL)
		entry = ctx->default_map;
	if (entry == NULL)
		r = idn__utf32_strcpy(to, tolen, from);
	else
		r = idn__foreignmap_map(entry->map, from, to, tolen);

	if (r == idn_success) {
		TRACE(("idn__langlocalmap_map(): succcess (to=\"%s\")\n",
		       idn__debug_utf32xstring(to)));
	} else {
		TRACE(("idn__langlocalmap_map(): %s\n",
		       idn_result_tostring(r)));
	}
	return (r);
}

/*
 * An entry point to 'mapproc' of the maplist module.
 */
idn_result_t
idn__langlocalmap_mapproc(void *ctx, const unsigned long *from,
			  unsigned long *to, size_t tolen) {
	return (idn__langlocalmap_map((idn__langlocalmap_t)ctx, from, to,
				      tolen));
}

/*
 * An entry point to 'destroyproc' of the maplist module.
 */
void
idn__langlocalmap_destroyproc(void *ctx) {
	idn__langlocalmap_destroy((idn__langlocalmap_t)ctx);
}

