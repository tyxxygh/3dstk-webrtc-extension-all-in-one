/*
 * Maximum label length of a TLD.
 */
#define IDN__TLDLOCALMAP_MAXTLDLENGTH		63

/*
 * Language localmap entry.
 */
typedef struct tldlocalmap_entry *idn__tldlocalmap_entry_t;

struct tldlocalmap_entry {
	unsigned long *tld;			/* tld name */
	idn__foreignmap_t map;  		/* map object */
	idn__tldlocalmap_entry_t next;		/* pointer to next entry */
};

/*
 * Language localmap object.
 */
struct idn__tldlocalmap {
	idn__strhash32_t hash;                 /* hash table for map
						* entries */
	idn__tldlocalmap_entry_t entries_head; /* map entries */
	idn__tldlocalmap_entry_t entries_tail;
	idn__tldlocalmap_entry_t default_map;  /* default map */
};

static idn_result_t	gettld(const unsigned long *name, unsigned long **tld);

/*
 * Create a localmap object.
 */
idn_result_t
idn__tldlocalmap_create(idn__tldlocalmap_t *ctxp) {
	idn_result_t r = idn_success;
	idn__tldlocalmap_t ctx = NULL;
	idn__strhash32_t new_hash = NULL;

	assert(ctxp != NULL);
	TRACE(("idn__tldlocalmap_create()\n"));

	ctx = (idn__tldlocalmap_t)malloc(sizeof(struct idn__tldlocalmap));
	if (ctx == NULL) {
		r = idn_nomemory;
		goto ret;
	}

	ctx->hash = NULL;
	ctx->entries_head = NULL;
	ctx->entries_tail = NULL;
	ctx->default_map  = NULL;

	r = idn__strhash32_create(&new_hash);
	if (r != idn_success)
		goto ret;
	ctx->hash = new_hash;

	*ctxp = ctx;

ret:
	if (r != idn_success) {
		free(new_hash);
		free(ctx);
	}
	TRACE(("idn__tldlocalmap_create(): %s\n", idn_result_tostring(r)));
	return (r);
}

/*
 * Release all the memory allocated for 'ctx'.
 */
void
idn__tldlocalmap_destroy(idn__tldlocalmap_t ctx) {
	idn__tldlocalmap_entry_t entry;
	idn__tldlocalmap_entry_t next;

	assert(ctx != NULL);

	TRACE(("idn__tldlocalmap_destroy()\n"));

	if (ctx->entries_head != NULL) {
		entry = ctx->entries_head;
		while (entry != NULL) {
			next = entry->next;
			free(entry->tld);
			idn__foreignmap_destroy(entry->map);
			free(entry);
			entry = next;
		}
	}

	idn__strhash32_destroy(ctx->hash, NULL);
	free(ctx);

	TRACE(("idn__tldlocalmap_destroy(): the object is destroyed\n"));
}

/*
 * Read a mapping definition file and add mappings in it to 'ctx'.
 */
idn_result_t
idn__tldlocalmap_addfromfile(idn__tldlocalmap_t ctx, const char *tld,
			     const char *file) {
	static const unsigned long default_map_tld[] = {
		'<', 'd', 'e', 'f', 'a', 'u', 'l', 't', '>', '\0'
	};
	idn_result_t r = idn_success;
	unsigned long tld_utf32[IDN__TLDLOCALMAP_MAXTLDLENGTH + 1];
	idn__tldlocalmap_entry_t new_entry = NULL;
	idn__foreignmap_t new_mapctx = NULL;

	assert(ctx != NULL && file != NULL);

	TRACE(("idn__tldlocalmap_addfromfile(tld=\"%s\", file=\"%s\")\n",
	       idn__debug_xstring(tld), idn__debug_xstring(file)));

	if (tld != NULL && *tld == '.')
		tld++;
	if (tld != NULL && *tld == '\0') {
		r = idn_invalid_syntax;
		goto ret;
	}
	if (tld != NULL && strcmp(tld, "*") == 0)
		tld = NULL;

	new_entry = (idn__tldlocalmap_entry_t)malloc(sizeof(*new_entry));
	if (new_entry == NULL) {
		r = idn_nomemory;
		goto ret;
	}
	new_entry->tld  = NULL;
	new_entry->map  = NULL;
	new_entry->next = NULL;

	r = idn__foreignmap_create(&new_mapctx);
	if (r != idn_success)
		goto ret;
	r = idn__foreignmap_addfromfile(new_mapctx, file);
	if (r != idn_success)
		goto ret;
	new_entry->map = new_mapctx;

	if (tld != NULL) {
		r = idn__utf32_fromutf8(tld, tld_utf32,
					IDN__TLDLOCALMAP_MAXTLDLENGTH + 1);
		if (r != idn_success) {
			r = idn_invalid_name;
			goto ret;
		}
		idn__utf32_asclower(tld_utf32);
		new_entry->tld = idn__utf32_strdup(tld_utf32);
		if (new_entry->tld == NULL) {
			r = idn_nomemory;
			goto ret;
		}

		r = idn__strhash32_put(ctx->hash, tld_utf32, new_entry);
		if (r != idn_success)
		goto ret;
	} else {
		new_entry->tld = idn__utf32_strdup(default_map_tld);
		if (new_entry->tld == NULL) {
			r = idn_nomemory;
			goto ret;
		}
		ctx->default_map = new_entry;
	}

	if (ctx->entries_head == NULL) {
		ctx->entries_head = new_entry;
		ctx->entries_tail = new_entry;
	} else {
		ctx->entries_tail->next = new_entry;
		ctx->entries_tail = new_entry;
	}
	
ret:
	if (r == idn_success) {
		TRACE(("idn__tldlocalmap_addfromfile(): success "
		       "(tld=\"%s\")\n", idn__debug_xstring(tld)));
	} else {
		TRACE(("idn__tldlocalmap_addfromfile(): %s\n",
		       idn_result_tostring(r)));
		if (new_mapctx != NULL)
			idn__foreignmap_destroy(new_mapctx);
		if (new_entry != NULL) {
			free(new_entry->tld);
			free(new_entry);
		}
	}
	return (r);
}

/*
 * Perform mapping.
 */
idn_result_t
idn__tldlocalmap_map(idn__tldlocalmap_t ctx, const unsigned long *from,
		     unsigned long *to, size_t tolen) {
	idn_result_t r = idn_success;
	unsigned long *tld = NULL;
	idn__tldlocalmap_entry_t entry = NULL;

	assert(ctx != NULL && from != NULL && to != NULL);

	TRACE(("idn__tldlocalmap_map(from=\"%s\", tolen=%d)\n",
	       idn__debug_utf32xstring(from), (int)tolen));

	r = gettld(from, &tld);
	if (r != idn_success)
		goto ret;

	entry = (idn__tldlocalmap_entry_t)idn__strhash32_get(ctx->hash, tld);
	if (entry == NULL && ctx->default_map != NULL)
		entry = ctx->default_map;
	if (entry == NULL)
		r = idn__utf32_strcpy(to, tolen, from);
	else
		r = idn__foreignmap_map(entry->map, from, to, tolen);

ret:
	if (r == idn_success) {
		TRACE(("idn__tldlocalmap_map(): succcess (to=\"%s\")\n",
		       idn__debug_utf32xstring(to)));
	} else {
		TRACE(("idn__tldlocalmap_map(): %s\n",
		       idn_result_tostring(r)));
	}
	free(tld);
	return (r);
}

static idn_result_t
gettld(const unsigned long *name, unsigned long **tld) {
	static const unsigned long hyphen_str[] = {'-', '\0'};
	const unsigned long *candidate = name;
	const unsigned long *fullstop;
	size_t tldlen;

	for (;;) {
		fullstop = idn__utf32_strchr(candidate, '.');
		if (fullstop == NULL || *(fullstop + 1) == '\0')
			break;
		candidate = fullstop + 1;
	}
	if (candidate == name && fullstop == NULL)
		candidate = hyphen_str;

	*tld = idn__utf32_strdup(candidate);
	if (*tld == NULL)
		return (idn_nomemory);
	tldlen = idn__utf32_strlen(*tld);
	if (tldlen > 0 && *(*tld + tldlen - 1) == '.')
		*(*tld + tldlen - 1) = '\0';
	idn__utf32_asclower(*tld);

	return (idn_success);
}

/*
 * An entry point to 'mapproc' of the maplist module.
 */
idn_result_t
idn__tldlocalmap_mapproc(void *ctx, const unsigned long *from,
			 unsigned long *to, size_t tolen) {
	return (idn__tldlocalmap_map((idn__tldlocalmap_t)ctx, from, to,
				      tolen));
}

/*
 * An entry point to 'destroyproc' of the maplist module.
 */
void
idn__tldlocalmap_destroyproc(void *ctx) {
	idn__tldlocalmap_destroy((idn__tldlocalmap_t)ctx);
}

